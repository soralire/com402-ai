#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Plot near-vs-remote memory comparison figures for cxl_numa_csma.

The default input is the clean CSV produced by run_near_remote.sh.  Figures and
summary tables are written under results/near_remote_memory/figures by default,
keeping this comparison separate from earlier result directories.
"""

from __future__ import annotations

import argparse
from pathlib import Path

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd


MODE_ORDER = ["random", "csma", "aimd"]
MODE_LABELS = {
    "random": "Random",
    "csma": "CSMA-like",
    "aimd": "AIMD",
}

NUMERIC_COLS = [
    "load",
    "seed",
    "attempts",
    "success",
    "retry",
    "backoff",
    "delay_p50",
    "delay_p95",
    "delay_p99",
    "goodput",
    "threads",
    "seconds",
    "mem_node",
    "cpu_node",
    "mem_mb",
    "touches_per_req",
    "latency_samples",
    "queue_depth",
    "device_workers",
    "avg_cwnd",
    "avg_inflight",
    "max_inflight",
]


def parse_args() -> argparse.Namespace:
    project_dir = Path(__file__).resolve().parents[2]
    default_results = project_dir / "results" / "near_remote_memory"
    parser = argparse.ArgumentParser(
        description="Generate figures comparing local and remote NUMA backend memory."
    )
    parser.add_argument(
        "--csv",
        type=Path,
        default=default_results / "near_remote_clean.csv",
        help="Input clean CSV generated by run_near_remote.sh.",
    )
    parser.add_argument(
        "--out-dir",
        type=Path,
        default=default_results / "figures",
        help="Output directory for figures and summary CSV files.",
    )
    parser.add_argument(
        "--format",
        dest="fmt",
        choices=["png", "pdf", "svg"],
        default="png",
        help="Figure format.",
    )
    parser.add_argument("--dpi", type=int, default=300, help="DPI for PNG output.")
    return parser.parse_args()


def read_results(path: Path) -> pd.DataFrame:
    if not path.exists():
        raise FileNotFoundError(f"CSV file does not exist: {path}")

    df = pd.read_csv(path)
    df = df[df["track"].astype(str).str.lower() != "track"].copy()
    df["track"] = df["track"].astype(str).str.strip().str.lower()

    for col in NUMERIC_COLS:
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors="coerce")

    required = {"track", "load", "mem_node", "cpu_node", "goodput", "delay_p50", "delay_p95", "delay_p99"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"CSV is missing required columns: {sorted(missing)}")

    df = df.dropna(subset=["track", "load", "mem_node", "cpu_node"]).copy()
    df["load"] = df["load"].astype(int)
    df["mem_node"] = df["mem_node"].astype(int)
    df["cpu_node"] = df["cpu_node"].astype(int)
    df["memory_kind"] = np.where(df["mem_node"] == df["cpu_node"], "local", "remote")
    df["memory_label"] = np.where(df["memory_kind"] == "local", "Local memory", "Remote memory")

    for col in ["delay_p50", "delay_p95", "delay_p99"]:
        df[f"{col}_ms"] = df[col] / 1_000_000.0

    df["retry_rate_pct"] = np.where(df["attempts"] > 0, df["retry"] / df["attempts"] * 100.0, np.nan)
    df["backoff_rate_pct"] = np.where(df["attempts"] > 0, df["backoff"] / df["attempts"] * 100.0, np.nan)
    df["tail_amp_p99_p50"] = np.where(df["delay_p50"] > 0, df["delay_p99"] / df["delay_p50"], np.nan)
    return df.reset_index(drop=True)


def ordered_modes(values: pd.Series) -> list[str]:
    present = list(dict.fromkeys(values.astype(str)))
    known = [mode for mode in MODE_ORDER if mode in present]
    unknown = sorted(mode for mode in present if mode not in MODE_ORDER)
    return known + unknown


def savefig(out_dir: Path, stem: str, fmt: str, dpi: int) -> Path:
    out_path = out_dir / f"{stem}.{fmt}"
    plt.tight_layout()
    plt.savefig(out_path, dpi=dpi, bbox_inches="tight")
    plt.close()
    return out_path


def aggregate(df: pd.DataFrame, metric: str) -> pd.DataFrame:
    return (
        df.groupby(["track", "load", "memory_kind", "memory_label"], as_index=False)[metric]
        .agg(mean="mean", std="std", count="count")
        .sort_values(["track", "load", "memory_kind"])
    )


def plot_metric_by_mode(
    df: pd.DataFrame,
    metric: str,
    ylabel: str,
    title: str,
    stem: str,
    out_dir: Path,
    fmt: str,
    dpi: int,
) -> Path | None:
    if metric not in df.columns:
        return None

    agg = aggregate(df.dropna(subset=[metric]), metric)
    if agg.empty:
        return None

    modes = ordered_modes(agg["track"])
    fig, axes = plt.subplots(1, len(modes), figsize=(5.2 * len(modes), 4.2), squeeze=False)
    styles = {
        "local": {"marker": "o", "linestyle": "-", "label": "Local memory"},
        "remote": {"marker": "s", "linestyle": "--", "label": "Remote memory"},
    }

    for ax, mode in zip(axes.ravel(), modes):
        sub_mode = agg[agg["track"] == mode]
        for memory_kind in ["local", "remote"]:
            sub = sub_mode[sub_mode["memory_kind"] == memory_kind].sort_values("load")
            if sub.empty:
                continue
            style = styles[memory_kind]
            ax.plot(
                sub["load"],
                sub["mean"],
                marker=style["marker"],
                linestyle=style["linestyle"],
                linewidth=2,
                label=style["label"],
            )
            if sub["count"].max() > 1 and sub["std"].notna().any():
                lower = sub["mean"] - sub["std"].fillna(0)
                upper = sub["mean"] + sub["std"].fillna(0)
                ax.fill_between(sub["load"].to_numpy(), lower.to_numpy(), upper.to_numpy(), alpha=0.12)

        ax.set_title(MODE_LABELS.get(mode, mode))
        ax.set_xlabel("Load (%)")
        ax.set_ylabel(ylabel)
        ax.grid(True, linestyle="--", alpha=0.45)
        ax.legend(fontsize=9)

    fig.suptitle(title, y=1.04, fontsize=14)
    return savefig(out_dir, stem, fmt, dpi)


def make_pair_table(df: pd.DataFrame) -> pd.DataFrame:
    metrics = [
        "goodput",
        "delay_p50_ms",
        "delay_p95_ms",
        "delay_p99_ms",
        "retry_rate_pct",
        "backoff_rate_pct",
        "tail_amp_p99_p50",
        "avg_cwnd",
        "avg_inflight",
        "max_inflight",
    ]
    present = [metric for metric in metrics if metric in df.columns]
    grouped = (
        df.groupby(["track", "load", "memory_kind"], as_index=False)[present]
        .mean(numeric_only=True)
    )

    local = grouped[grouped["memory_kind"] == "local"].drop(columns=["memory_kind"])
    remote = grouped[grouped["memory_kind"] == "remote"].drop(columns=["memory_kind"])
    paired = local.merge(remote, on=["track", "load"], suffixes=("_local", "_remote"))

    for metric in present:
        local_col = f"{metric}_local"
        remote_col = f"{metric}_remote"
        if local_col in paired.columns and remote_col in paired.columns:
            paired[f"{metric}_remote_over_local"] = np.where(
                paired[local_col] != 0,
                paired[remote_col] / paired[local_col],
                np.nan,
            )
    return paired.sort_values(["track", "load"]).reset_index(drop=True)


def plot_remote_over_local(
    pair_table: pd.DataFrame,
    metric_ratio: str,
    ylabel: str,
    title: str,
    stem: str,
    out_dir: Path,
    fmt: str,
    dpi: int,
) -> Path | None:
    if metric_ratio not in pair_table.columns or pair_table.empty:
        return None

    plt.figure(figsize=(8, 5))
    for mode in ordered_modes(pair_table["track"]):
        sub = pair_table[pair_table["track"] == mode].sort_values("load")
        if sub.empty:
            continue
        plt.plot(sub["load"], sub[metric_ratio], marker="o", linewidth=2, label=MODE_LABELS.get(mode, mode))

    plt.axhline(1.0, color="black", linewidth=1, linestyle=":", alpha=0.8)
    plt.xlabel("Load (%)")
    plt.ylabel(ylabel)
    plt.title(title)
    plt.grid(True, linestyle="--", alpha=0.45)
    plt.legend()
    return savefig(out_dir, stem, fmt, dpi)


def plot_summary_bars(pair_table: pd.DataFrame, out_dir: Path, fmt: str, dpi: int) -> Path | None:
    ratio_cols = {
        "delay_p99_ms_remote_over_local": "p99 latency",
        "goodput_remote_over_local": "goodput",
        "retry_rate_pct_remote_over_local": "retry rate",
    }
    available = [col for col in ratio_cols if col in pair_table.columns]
    if pair_table.empty or not available:
        return None

    summary = pair_table.groupby("track", as_index=False)[available].mean(numeric_only=True)
    modes = ordered_modes(summary["track"])
    x = np.arange(len(modes))
    width = 0.8 / len(available)

    plt.figure(figsize=(8.5, 4.8))
    for idx, col in enumerate(available):
        sub = summary.set_index("track").reindex(modes)
        offsets = x - 0.4 + width / 2 + idx * width
        plt.bar(offsets, sub[col].to_numpy(), width=width, label=ratio_cols[col])

    plt.axhline(1.0, color="black", linewidth=1, linestyle=":", alpha=0.8)
    plt.xticks(x, [MODE_LABELS.get(mode, mode) for mode in modes])
    plt.ylabel("Remote / local ratio")
    plt.title("Average remote-over-local ratio by policy")
    plt.grid(True, axis="y", linestyle="--", alpha=0.45)
    plt.legend()
    return savefig(out_dir, "summary_remote_over_local_ratios", fmt, dpi)


def main() -> None:
    args = parse_args()
    args.out_dir.mkdir(parents=True, exist_ok=True)

    df = read_results(args.csv)
    pair_table = make_pair_table(df)

    generated: list[Path] = []
    summary_path = args.out_dir / "paired_near_remote_summary.csv"
    pair_table.to_csv(summary_path, index=False)
    generated.append(summary_path)

    metric_specs = [
        ("goodput", "Goodput (requests/s)", "Near vs remote goodput", "near_remote_goodput"),
        ("delay_p50_ms", "p50 latency (ms)", "Near vs remote p50 latency", "near_remote_p50_latency"),
        ("delay_p95_ms", "p95 latency (ms)", "Near vs remote p95 latency", "near_remote_p95_latency"),
        ("delay_p99_ms", "p99 latency (ms)", "Near vs remote p99 latency", "near_remote_p99_latency"),
        ("retry_rate_pct", "Retry rate (%)", "Near vs remote retry rate", "near_remote_retry_rate"),
        ("backoff_rate_pct", "Backoff rate (%)", "Near vs remote backoff rate", "near_remote_backoff_rate"),
        ("tail_amp_p99_p50", "p99 / p50", "Near vs remote tail amplification", "near_remote_tail_amplification"),
    ]

    for metric, ylabel, title, stem in metric_specs:
        path = plot_metric_by_mode(df, metric, ylabel, title, stem, args.out_dir, args.fmt, args.dpi)
        if path is not None:
            generated.append(path)

    ratio_specs = [
        ("delay_p99_ms_remote_over_local", "p99 latency remote / local", "Remote-over-local p99 latency ratio", "ratio_p99_remote_over_local"),
        ("goodput_remote_over_local", "goodput remote / local", "Remote-over-local goodput ratio", "ratio_goodput_remote_over_local"),
        ("retry_rate_pct_remote_over_local", "retry rate remote / local", "Remote-over-local retry-rate ratio", "ratio_retry_remote_over_local"),
    ]
    for ratio_col, ylabel, title, stem in ratio_specs:
        path = plot_remote_over_local(pair_table, ratio_col, ylabel, title, stem, args.out_dir, args.fmt, args.dpi)
        if path is not None:
            generated.append(path)

    path = plot_summary_bars(pair_table, args.out_dir, args.fmt, args.dpi)
    if path is not None:
        generated.append(path)

    manifest = args.out_dir / "figure_manifest.txt"
    with manifest.open("w", encoding="utf-8") as f:
        f.write("Near vs remote memory figures\n")
        f.write(f"Input CSV: {args.csv}\n")
        f.write(f"Rows: {len(df)}\n\n")
        for path in generated:
            f.write(path.name + "\n")
    generated.append(manifest)

    print(f"Loaded {len(df)} rows from {args.csv}")
    print(f"Generated {len(generated)} files under {args.out_dir}")
    for path in generated:
        print(f"  - {path.name}")


if __name__ == "__main__":
    main()
